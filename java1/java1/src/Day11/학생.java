package Day11;

public class 학생 {

	int 번호;
	String 이름;
	int 국어;
	int 영어;
	int 수학;
	int 총점;
	
	public 학생() {}
	
	public 학생(int 번호, String 이름, int 국어, int 영어, int 수학, int 총점) {
		super();
		this.번호 = 번호;
		this.이름 = 이름;
		this.국어 = 국어;
		this.영어 = 영어;
		this.수학 = 수학;
		this.총점 = 총점;
	}
	
	
	
}
